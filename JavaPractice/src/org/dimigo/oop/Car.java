public class Car {
	private String company, model, color;
	private int maxSpeed, price;

	public String getCompany() {
		return company;
	}
	
	public String getModel() {
		return model;
	}
	
	public String getColor() {
		return color;
	}
	
	public int getMaxSpeed() {
		return maxSpeed;
	}
	
	public int getPrice() {
		return price;
	}
	
	public void setCompany(String arg1) {
		company = arg1;
	}
	
	public void setModel(String arg1) {
		model = arg1;
	}
	
	public void setColor(String arg1) {
		color = arg1;
	}
	
	public void setMaxSpeed(int arg1) {
		maxSpeed = arg1;
	}
	
	public void setPrice(int arg1) {
		price = arg1;
	}
}
